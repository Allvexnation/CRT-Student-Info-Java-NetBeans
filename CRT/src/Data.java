    private void ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDActionPerformed
        DefaultTableModel table = (DefaultTableModel) TL1.getModel();
        String N = NAME.getText().trim();
        String SE = SECT.getText().trim();
        String SU = SUB.getText().trim();
        
        if (N.isEmpty() || SE.isEmpty() || SU.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Please fill it up", "Message", JOptionPane.ERROR_MESSAGE);
            return;
        }  else {
        for (int i = 0; i < table.getRowCount(); i++) {
                if (table.getValueAt(i, 0).equals(N) && table.getValueAt(i, 1).equals(SE) && table.getValueAt(i, 2).equals(SU)) {
                 JOptionPane.showMessageDialog(null,"This data is already existed", "Message", JOptionPane.ERROR_MESSAGE);
                return;
            }
               
            }
        }
            
        
        table.addRow(new Object[] {N, SE, SU});
            NAME.setText("");
            SECT.setText("");
            SUB.setText("");
            isUpdating = false;
    }//GEN-LAST:event_ADDActionPerformed

