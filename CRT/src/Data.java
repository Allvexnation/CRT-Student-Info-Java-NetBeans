    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
DefaultTableModel table = (DefaultTableModel) TL1.getModel();
  int row = TL1.getSelectedRow();
      if (row != -1) {
           if (JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Are you sure/", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
          table.removeRow(row);
          NAME.setText("");
            SECT.setText("");
            SUB.setText("");
            JOptionPane.showMessageDialog(null,"Data has been updated", "Message", JOptionPane.INFORMATION_MESSAGE);
      }
      } else {
          JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Warning", JOptionPane.ERROR_MESSAGE);
      }
     
  
    }//GEN-LAST:event_jButton9ActionPerformed

