    private void TL1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TL1MouseClicked
        int row = TL1.getSelectedRow();
        if (row != -1) { 
            
            String name = TL1.getValueAt(row, 0).toString();
            String section = TL1.getValueAt(row, 1).toString();
            String subject = TL1.getValueAt(row, 2).toString();
            
          
            NAME.setText(name);
            SECT.setText(section);
            SUB.setText(subject);
            NAME.setEnabled(false);
            SECT.setEnabled(false);
            SUB.setEnabled(false);
            
            isUpdating = true;
        }
    }//GEN-LAST:event_TL1MouseClicked

